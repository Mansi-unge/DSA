public class UpperLowerCase {
  public static void main(String[] args) {
    char Upper = 'A' ;
    char lower = 'a' ;

    int difference =(int)lower - (int)Upper ;
    System.out.println("ASCII Code of A is : " + (int)Upper);
    System.out.println("ASCII Code of a is : " + (int)lower);
    System.out.println("Diffrence between A and a is : " + difference);
  }
  
}
